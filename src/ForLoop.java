
public class ForLoop {

	public static void main(String[] args) {
		
		String[] name = {"amit","Geet","Ahire","Geetraj"};
		//Enhanced for loop
		for(String s:name)
		{
			System.out.println(s);
		}
		
		int[] arr = {1,4,6,8,9,6,7,8};
		//for loop
		for(int i=0;i<arr.length;i++)
			{
				System.out.println(arr[i]);
			}
					
		

	}

}
